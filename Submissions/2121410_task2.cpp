#include <iostream>
using namespace std;

int main() 
{    
    cout << "the Size of char : " << sizeof(char) << " byte" << endl; //char
    cout << "the Size of int: " << sizeof(int) << " bytes" << endl;   //int 
    cout << "the Size of float: " << sizeof(float) << " bytes" << endl; //float
    cout << "the Size of double: " << sizeof(double) << " bytes" << endl;  //double

    return 0;
}
